public class Result {

    private double avg_time;
    private double avg_distance;
    private double avg_ascent;

    public void setAvg_time(double avg_time) {
        this.avg_time = avg_time;
    }

    public void setAvg_distance(double avg_distance) {
        this.avg_distance = avg_distance;
    }

    public void setAvg_ascent(double avg_ascent) {
        this.avg_ascent = avg_ascent;
    }

    public double getAvg_time() {
        return avg_time;
    }

    public double getAvg_distance() {
        return avg_distance;
    }

    public double getAvg_ascent() {
        return avg_ascent;
    }
}
